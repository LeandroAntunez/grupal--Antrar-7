package ar.edu.unq.eis.antrar.backend.model;

public class Localidad {
	
	private String nombre;
	private float horas;
	
	
	public Localidad (){
		
		
	}


	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


	public float getHoras() {
		return horas;
	}


	public void setHoras(float horas) {
		this.horas = horas;
	}
	

}
